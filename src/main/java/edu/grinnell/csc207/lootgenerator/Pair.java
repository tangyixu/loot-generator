package edu.grinnell.csc207.lootgenerator;

/**
 *
 * @author Tiffany Tang
 */
public class Pair {

    private int min;
    private int max;

    public Pair(int min, int max) {
        this.min = min;
        this.max = max;
    }

    public int getMin() {
        return this.min;
    }

    public int getMax() {
        return this.max;
    }

}
